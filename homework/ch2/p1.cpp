/* Sum of Two Numbers
 *   Write a program that stores the integers 62 and 99 in variables, and stores the sum of
 *   these two in a variable named total.
 */
#include <iostream>

int main() {
  int a = 62, b = 99;

  int total = a + b;

  // To test:
  std::cout << "a + b = " << total << std::endl;
}
